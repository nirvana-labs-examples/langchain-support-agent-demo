"""
Aggregate per-platform JSON results into a markdown comparison table.

Reads results/<platform>/ingest_<dataset>.json, retrieval_<dataset>.json, and
fio.json for every platform directory under results/ and writes
results/comparison_<dataset>.md.

Usage: compare-results.py [dataset]   (default: medium)

Run after the Ansible playbook has fetched JSON files back to results/.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

_REPO_ROOT = Path(__file__).resolve().parent.parent.parent
_RESULTS_DIR = _REPO_ROOT / "results"

_PLATFORM_ORDER = ["gp3-3k", "gp3-16k", "io2-32k", "io2-64k", "nirvana-abs"]


def _load(platform_dir: Path, name: str) -> dict[str, object] | None:
    path = platform_dir / f"{name}.json"
    if not path.exists():
        return None
    data: dict[str, object] = json.loads(path.read_text())  # pyright: ignore[reportAny]
    return data


def _fmt(v: object, suffix: str = "") -> str:
    if v is None:
        return "—"
    if isinstance(v, (int, float)):
        return f"{v:,.0f}{suffix}" if suffix else f"{v:,}"
    return str(v)


def main() -> None:
    dataset = sys.argv[1] if len(sys.argv) > 1 else "medium"

    if not _RESULTS_DIR.exists():
        print(f"No results/ directory at {_RESULTS_DIR}")
        return

    # Collect platforms (in known order, then any extras alphabetically)
    found = {p.name for p in _RESULTS_DIR.iterdir() if p.is_dir()}
    platforms = [p for p in _PLATFORM_ORDER if p in found] + sorted(
        p for p in found if p not in _PLATFORM_ORDER
    )

    if not platforms:
        print("No platform directories found under results/")
        return

    rows: list[dict[str, object | None]] = []
    for platform in platforms:
        d = _RESULTS_DIR / platform
        ingest = _load(d, f"ingest_{dataset}") or {}
        retrieval = _load(d, f"retrieval_{dataset}") or {}
        fio = _load(d, "fio") or {}
        rows.append({
            "platform": platform,
            "fio_iops": fio.get("iops"),
            "fio_lat_us": fio.get("latency_us"),
            "ingest_throughput": ingest.get("write_throughput_vec_per_sec"),
            "ingest_p50": ingest.get("batch_latency_p50_ms"),
            "ingest_p95": ingest.get("batch_latency_p95_ms"),
            "ingest_p99": ingest.get("batch_latency_p99_ms"),
            "retrieval_p50": retrieval.get("search_latency_p50_ms"),
            "retrieval_p95": retrieval.get("search_latency_p95_ms"),
            "retrieval_p99": retrieval.get("search_latency_p99_ms"),
        })

    lines: list[str] = []
    lines.append(f"# AWS vs Nirvana benchmark comparison ({dataset})\n")
    lines.append(
        "Generated by `infra/scripts/compare-results.py` from per-platform JSON files in `results/`.\n"
    )

    lines.append("## Raw disk (fio, 4k randread)\n")
    lines.append("| Platform | IOPS | Latency (μs) |")
    lines.append("|---|---:|---:|")
    for r in rows:
        lines.append(f"| {r['platform']} | {_fmt(r['fio_iops'])} | {_fmt(r['fio_lat_us'])} |")

    lines.append("\n## Ingest (Qdrant write throughput)\n")
    lines.append("| Platform | Throughput (vec/s) | Batch p50 (ms) | p95 | p99 |")
    lines.append("|---|---:|---:|---:|---:|")
    for r in rows:
        lines.append(
            f"| {r['platform']} | {_fmt(r['ingest_throughput'])} | {_fmt(r['ingest_p50'])} | {_fmt(r['ingest_p95'])} | {_fmt(r['ingest_p99'])} |"
        )

    lines.append("\n## Retrieval latency (Qdrant HNSW search only)\n")
    lines.append("| Platform | p50 (ms) | p95 | p99 |")
    lines.append("|---|---:|---:|---:|")
    for r in rows:
        lines.append(
            f"| {r['platform']} | {_fmt(r['retrieval_p50'])} | {_fmt(r['retrieval_p95'])} | {_fmt(r['retrieval_p99'])} |"
        )

    out = _RESULTS_DIR / f"comparison_{dataset}.md"
    _ = out.write_text("\n".join(lines) + "\n")
    try:
        rel = out.relative_to(_REPO_ROOT)
        print(f"Wrote {rel}")
    except ValueError:
        print(f"Wrote {out}")


if __name__ == "__main__":
    main()
